// Save Color to Local Storage
let mainColors = localStorage.getItem("color_option")
if (mainColors !== null){
    document.documentElement.style.setProperty("--green", mainColors);

    // Remove All Active Class From Li
        document.querySelectorAll(".colors-list li").forEach(element =>{
            element.classList.remove("active");
          //  Add Active Class if data set = Local Storage Color
            if (element.dataset.color === mainColors){
                element.classList.add("active")
            }
        })
    
}



document.querySelector(".toggle-settings .icon ").onclick = function () {
  this.classList.toggle("fa-spin");
  // Adding Open Class to the Box
  document.querySelector(".setting-box").classList.toggle("open");
};

let colorsLi = document.querySelectorAll(".colors-list li ");
//  When Click on each Color
colorsLi.forEach((li) => {
  li.addEventListener("click", (e) => {
    // e.target.dataset.color;
    // Set Color To Root
    document.documentElement.style.setProperty(
      "--green",
      e.target.dataset.color
    );

    //  Set color To Local Storage
    localStorage.setItem("color_option", e.target.dataset.color);

    // Remove All Active Class From Li
    e.target.parentElement.querySelectorAll(".active").forEach((element) => {
      element.classList.remove("active");
    });
    // Add active on Clicked li
    e.target.classList.add("active");
  });
});
